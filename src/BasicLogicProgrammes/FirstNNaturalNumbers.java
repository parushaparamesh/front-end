package BasicLogicProgrammes;


public class FirstNNaturalNumbers {

	public static void main(String[] args) {
		
//By using for loop

		  int n = 10;
		  
//		  declare the sum
		  int sum = 0;

		  for (int i = 1; i <= n; i++)
			  // incrementing the sum upto n
		         sum += i;
		       System.out.println (sum);
	}

}


